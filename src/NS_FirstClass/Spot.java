package NS_FirstClass;

public class Spot {
    private int Number_of_Spot = 0;
    private String Car = "";
    private boolean Status = true;

    public int getNumber_of_Spot() {
        return Number_of_Spot;
    }

    public void setNumber_of_Spot(int number_of_Spot) {
        Number_of_Spot = number_of_Spot;
    }

    public String getCar() {
        return Car;
    }

    public void setCar(String car) {
        Car = car;
    }

    public boolean isStatus() {
        return Status;
    }

    public void setStatus(boolean status) {
        Status = status;
    }



}
