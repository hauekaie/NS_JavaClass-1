package NS_FirstClass;

public class ParkingLot {
    private String Spot = "";
    private String Name = "";


    public String getSpot() {
        return Spot;
    }

    public void setSpot(String spot) {
        Spot = spot;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
