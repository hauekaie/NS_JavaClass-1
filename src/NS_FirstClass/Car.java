package NS_FirstClass;

public class Car {
    private String Plates = "";
    private String Type = "";


    public String getPlates() {
        return Plates;
    }

    public void setPlates(String plates) {
        Plates = plates;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }
}

